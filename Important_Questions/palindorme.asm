CODE SEGMENT
     ASSUME CS:CODE,DS:DATA,ES:EXTRA
    
    START:
    MOV AX,DATA
    MOV DS,AX
    MOV AX,EXTRA
    MOV ES,AX

    LEA SI,ARR1
    LEA DI,ARR2+04H

    MOV CX,05H
   LOOP1: 
    CLD           ; HERE THE IP WILL AUTO INCREMENT
    LODSB 
    STD           ; HERE THE DI WILL AUTO DECEREMENT , BECAUSE WE WANT TO STORE THE STRING IN ES IN REVERSE MANNER
    STOSB
    LOOP LOOP1

; TILL HERE IT WAS A PROGRAMME TO REVERSE A STRING

; NOW PROGRAMME TO COMPARE TWO STRINGS

   
   MOV CX,005H
   LEA SI,ARR1
   LEA DI,ARR2
   CLD 
   REPE CMPSB   ; CONDITIONAL  (REPEATE ON EQUAL)
   JNZ SKIP
   INC PAL
    

   
