DATA SEGMENT
    ORG 2000h
    ARR1 DB 01H,02H,04H,04H,05H,06H,07H,08H
    TOT DB ?
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE ,DS:DATA
    START:
    MOV AX,DATA
    MOV DS,AX
    LEA SI,ARR1
    XOR AX,AX
    XOR BX,BX
    MOV CX,08H
    XOR DX,DX

LOOP1:
    MOV AX,[SI]
    AND AX,01H
    JNZ LABEL1
    INC SI
    ADD BL,[SI]
    INC DX
    LOOP LOOP1
    CMP CX,00H
    JZ LABEL2



LABEL1:
  INC SI
  LOOP LOOP1

LABEL2:
   MOV SI,5000H
   MOV [SI],BL
   INC SI
   MOV [SI],BH
HLT
CODE ENDS
END START